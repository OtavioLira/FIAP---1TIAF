def exibirTabela() -> None:
    for linha in agricultura_brasileira:
        for item in linha:
            print(f"{item}: {linha[item]}")
        print()

# Essa função verifica se as informações do territorio do agricultor bate com a base de dados
def verificar(tmp_colheita = None, maquinario = None, clima = None) -> list: 
    resp = []
    for linha in agricultura_brasileira:
        if tmp_colheita in linha["tempo_colheita"]:
            if maquinario in linha["maquinario"]:
                if clima in linha["clima"]:
                    resp.append(linha)
    return resp

# Essa função procura se uma lista está dentro da base de dados
def procurar(lista: str) -> str:
    resp = ""
    for linha_tabela in agricultura_brasileira:
        for linha_lista in lista:
            if linha_lista == linha_tabela:
                for item in linha_tabela:
                    resp += f"\n{item}: {linha_tabela[item]}"
    return resp       

def menu() -> None:
    tmp_colheita = input("Digite o tempo de colheita: ")
    maquinario = input("Possui maquinario? ")
    clima = input("Digite o clima: ")
    resp = verificar(tmp_colheita, maquinario, clima)
    resp = procurar(resp)
    print(f"""--------Respostas semelhantes--------
    {resp}
    --------------------------------""")

menu()